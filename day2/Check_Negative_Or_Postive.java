package week1.day2;

public class Check_Negative_Or_Postive {

	public static void main(String[] args) {
		int num = -43;
		
		if(num > 0) {
			System.out.println(num + " : is Postive number");
		}
		else if(num < 0) {
			System.out.println(num + " : is Negative number");
		}
		else {
			System.out.println(num + " : is Zero");
		}

	}

}
